a = int(input("Enter a number: "))
b = int(input("Enter another number: "))

print("Bitwise add: ",a&b)

print("Bitwise Or: ",a|b)

print("Bitwise Xor: ",a^b)

print("Bitwise negation of a: ",~a)

print("Bitwise negation of b: ",~b)

print("Left shift of a by 1: ",a<<1)

print("Right shift of a by 1: ",a>>1)

print("Left shift of b by 1: ",b<<1)

print("Right shift of b by 1: ",b>>1)

